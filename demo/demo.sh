#!/bin/bash
echo "------ Case 1: 2016-01-09 17:20:01 ~ 2016-01-09 17:20:03 ------"
./timecat -s '2016-01-09 17:00:00' -e '2016-01-09 17:20:03' ./demo.log
echo ""

echo "------ Case 2: 2016-01-09 17:19:56 ~ 2016-01-09 17:20:00 ------"
./timecat -d '2016-01-09' -s '17:19:56' -e '17:20:00' ./demo.log
echo ""

echo "------ Case 2: 2016-01-09 17:20:10 ~ EOF ------"
./timecat -d '2016-01-09' -s '17:20:10' ./demo.log
echo ""
